package com.siemens.training.java.adv;

public class Adv {
	private String advText;
	private EGender genderCheck;
	private Integer ageMin;
	private Integer ageMax;
	private EHairColor hairColorCheck;
	private Integer heightMin;
	private Integer heightMax;
	public String getAdvText() {
		return advText;
	}
	public void setAdvText(String advText) {
		this.advText = advText;
	}
	public EGender getGenderCheck() {
		return genderCheck;
	}
	public void setGenderCheck(EGender genderCheck) {
		this.genderCheck = genderCheck;
	}
	public Integer getAgeMin() {
		return ageMin;
	}
	public void setAgeMin(Integer ageMin) {
		this.ageMin = ageMin;
	}
	public Integer getAgeMax() {
		return ageMax;
	}
	public void setAgeMax(Integer ageMax) {
		this.ageMax = ageMax;
	}
	public EHairColor getHairColorCheck() {
		return hairColorCheck;
	}
	public void setHairColorCheck(EHairColor hairColorCheck) {
		this.hairColorCheck = hairColorCheck;
	}
	public Integer getHeightMin() {
		return heightMin;
	}
	public void setHeightMin(Integer heightMin) {
		this.heightMin = heightMin;
	}
	public Integer getHeightMax() {
		return heightMax;
	}
	public void setHeightMax(Integer heightMax) {
		this.heightMax = heightMax;
	}



}
